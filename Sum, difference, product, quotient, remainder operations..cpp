#include <stdio.h>  
int main() {  
    int so1,so2;  
    scanf("%d %d",&so1 ,&so2);  
    printf("%d+%d=%d\n",so1 ,so2 , so1+so2);  
    printf("%d-%d=%d\n",so1 ,so2 , so1-so2);  
    printf("%d*%d=%d\n",so1 ,so2 , so1*so2);  
    printf("%d/%d=%d...%d\n",so1 ,so2 , so1/so2 , so1%so2);  
} 
